#include <stdio.h>

int main() {

    int number = 7;  
    
    if (number > 5) {
        // This block runs if the condition is true
        printf("Number is greater than 5\n");
    } else {
        // This block runs if the condition is false
        printf("Number is 5 or less\n");
    }

    return 0;  
}
